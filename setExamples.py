### unique elements
### changeable

setEx = set() #creates a set

listEx = [1, "hello", "yagmur", 9]
setEx = set(listEx)
print(setEx) #prints {1, 'hello', 'yagmur', 9}


tupleEx = (1, "hello", "yagmur", 9)
setEx = set(tupleEx)
print(setEx) #prints {1, 'hello', 'yagmur', 9}



